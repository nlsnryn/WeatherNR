<template>
  <div class="flex flex-col flex-1 items-center">
    <!--Banner-->
    <div
      v-if="route.query.preview"
      class="w-full bg-weather-secondary text-center p-4"
    >
      <p class="text-white">
        You are currently previewing this city, click the "+" icon to start
        tracking this city
      </p>
    </div>

    <!--Weather Overview-->
    <div class="flex flex-col text-white items-center py-12">
      <h1 class="text-2xl sm:text-4xl mb-2">{{ route.params.city }}</h1>
      <p class="text-xs sm:text-sm mb-12">
        {{
          new Date(weatherData.currentTime).toLocaleDateString("en-us", {
            weekday: "short",
            day: "2-digit",
            month: "long",
          })
        }}
        {{
          new Date(weatherData.currentTime).toLocaleTimeString("en-us", {
            timeStyle: "short",
          })
        }}
      </p>
      <h1 class="text-6xl sm:text-8xl mb-8">
        {{ Math.round(weatherData.current.temp) }}&deg;F
      </h1>
      <div class="text-center">
        <p class="text-xs sm:text-sm mb-2">
          Feels like {{ weatherData.current.feels_like }} &deg;F
        </p>
        <p class="capitalize text-xs sm:text-sm">
          {{ weatherData.current.weather[0].description }}
        </p>
        <img
          class="h-auto w-[110px] md:w-[150px] object-cover"
          :src="`http://openweathermap.org/img/wn/${weatherData.current.weather[0].icon}@2x.png`"
          alt=""
        />
      </div>
    </div>

    <hr class="border-white border-opacity-10 border w-full" />

    <!--Hourly Weather-->
    <div class="max-w-screen-md w-full py-12">
      <div class="mx-8 text-white">
        <h1 class="mb-4">Hourly Weather</h1>
        <div
          class="flex gap-10 overflow-x-scroll scrollbar-thin scrollbar-track-neutral-50 scrollbar-thumb-weather-secondary scroll-smooth scroll-mt-3"
        >
          <div
            v-for="hourData in weatherData.hourly"
            :key="hourData.dt"
            class="flex flex-col gap-4 items-center"
          >
            <p class="whitespace-nowrap text-sm">
              {{
                new Date(hourData.currentTime).toLocaleTimeString("en-us", {
                  hour: "numeric",
                })
              }}
            </p>
            <img
              class="w-auto h-[50px] object-cover"
              :src="`http://openweathermap.org/img/wn/${hourData.weather[0].icon}@2x.png`"
              alt=""
            />
            <p class="text-sm mb-2">{{ Math.round(hourData.temp) }}&deg;F</p>
          </div>
        </div>
      </div>
    </div>

    <hr class="border-white border-opacity-10 border w-full" />

    <!--Weekly Weather-->
    <div class="max-w-screen-md w-full py-12">
      <div class="mx-8 text-white">
        <h2 class="mb-4">7 Day Forecast</h2>
        <div
          v-for="day in weatherData.daily"
          :key="day.dt"
          class="flex items-center"
        >
          <p class="flex-1 text-sm">
            {{
              new Date(day.dt * 1000).toLocaleDateString("en-us", {
                weekday: "long",
              })
            }}
          </p>
          <img
            class="w-[50px] h-[50px] object-cover"
            :src="`http://openweathermap.org/img/wn/${day.weather[0].icon}@2x.png`"
            alt=""
          />
          <div class="flex gap-2 flex-1 justify-end">
            <p class="text-sm">H: {{ Math.round(day.temp.max) }} &deg;F</p>
            <p class="text-sm">L: {{ Math.round(day.temp.min) }} &deg;F</p>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script setup>
import axios from "axios";
import { useRoute } from "vue-router";

const route = useRoute();
const getWeatherData = async () => {
  try {
    const weatherData = await axios.get(
      `https://api.openweathermap.org/data/2.5/onecall?lat=${route.query.lat}&lon=${route.query.lng}&exclude={part}&appid=7efa332cf48aeb9d2d391a51027f1a71&units=imperial`
    );

    // cal current date & time
    const localOffset = new Date().getTimezoneOffset() * 60000;
    const utc = weatherData.data.current.dt * 1000 + localOffset;
    weatherData.data.currentTime =
      utc + 1000 * weatherData.data.timezone_offset;

    // cal hourly weather offset
    weatherData.data.hourly.forEach((hour) => {
      const utc = hour.dt * 1000 + localOffset;
      hour.currentTime = utc + 1000 * weatherData.data.timezone_offset;
    });

    // Flicker Delay

    await new Promise((res) => setTimeout(res, 1000));

    return weatherData.data;
  } catch (err) {
    console.log(err);
  }
};

const weatherData = await getWeatherData();
</script>
